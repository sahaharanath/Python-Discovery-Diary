# P06-data_types.py
# Integer data type
a = 12345
print(a)

# Float data type
b = 5.25
print(b)

# String data type
name = "Haranath Saha"
print(name)

# Complex data type
# Note: Complex numbers are represented as complex(real, imaginary)
c = complex(8, 2)
print(c)

# To know about the data type, use 'type()'
print("The data type of 'a' is:", type(a))
print("The data type of 'b' is:", type(b))
print("The data type of 'name' is:", type(name))
print("The data type of 'c' is:", type(c))
