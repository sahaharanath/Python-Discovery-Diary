# P13-Assignment Operators.py
# Demonstrates various assignment operators in Python.

# Example 1: '=' Operator
print("'=' Operator")
x = 5
print(x)

# Example 2: '+=' Operator
print("'+=' Operator")
a = 5
a += 4  # Equivalent to a = a + 4
print(a)

# Example 3: '-=' Operator
print("-=' Operator")
b = 6
b -= 2  # Equivalent to b = b - 2
print(b)
