# P16-Membership Operators.py
# Demonstrates membership operators in Python.

# Example 1: 'in' Operator
print("'in' Operator")
str1 = "Hello Welcome to Python Code With me"
print('h' in str1)  # True, as 'h' is present in the string

# Example 2: 'not in' Operator
print("'not in' Operator")
str2 = "Hello Welcome to Kolkata"
print('t' not in str2)  # False, as 't' is present in the string
