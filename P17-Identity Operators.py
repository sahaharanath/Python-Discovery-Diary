# P17-identity_operators.py
# Demonstrates identity operators in Python.

# Example 1: 'is' Operator
print("'is' Operator")
x = "What is your favorite color?"
y = "What is your favorite food?"
print(x is y)  # False, as x and y have different values

# Example 2: 'is not' Operator
print("'is not' Operator")
x1 = "50"
y1 = "20"
print(x1 is not y1)  # True, as x1 and y1 have different values
