# P08-Arithmetic Operations.py
# Demonstrates arithmetic operations on variables a and b with comments for clarity.
a = 5215
b = 25

# Addition
print("The value of", a, "+", b, "is equal to:", a + b)
print(a, "+", b, "=", a + b)

# Subtraction
print("The value of", a, "-", b, "is equal to:", a - b)
print(a, "-", b, "=", a - b)

# Multiplication
print("The value of", a, "*", b, "is equal to:", a * b)
print(a, "*", b, "=", a * b)

# Division
print("The value of", a, "/", b, "is equal to:", a / b)
print(a, "/", b, "=", a / b)

# Modulus (Remainder)
print("The value of", a, "%", b, "is equal to:", a % b)
print(a, "%", b, "=", a % b)

# Floor Division
print("The value of", a, "//", b, "is equal to:", a // b)
print(a, "//", b, "=", a // b)

# Exponentiation
print("The value of", a, "**", b, "is equal to:", a ** b)
print(a, "**", b, "=", a ** b)
