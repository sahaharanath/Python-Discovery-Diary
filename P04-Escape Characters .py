# P04-escape_characters.py
# We use \n to separate lines, and \n is considered as one character, not 2 characters
print("Here is the first line of code \nNow here is the second line of code!")

# To handle quotes within a string, use \"
print("She said, \"That she is a good girl.\" And I knew it before!")
# Here also \" is treated as a single character!

# For single quote (') use \'
print('This is \'PyCharm\' And It is amazing')
